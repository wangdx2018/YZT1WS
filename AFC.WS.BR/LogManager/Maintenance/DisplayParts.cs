using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace AFC.WS.BR.Maintenance
{
   public class DisplayParts
    {
       string _part_id;
       string _mc_dep_name;
       string _dev_part_cn_name;
       string _status;
       string _check_out_operator;


       public string part_id
       {
           get { return _part_id; }
           set { _part_id = value; }
       }

       public string mc_dep_name
       {
           get { return _mc_dep_name; }
           set { _mc_dep_name = value; }
       }
       public string dev_part_cn_name
       {
           get { return _dev_part_cn_name; }
           set { _dev_part_cn_name = value; }
       }

       public string status
       {
           get { return _status; }
           set { _status = value; }
       }

       public string check_out_operator
       {
           get { return _check_out_operator; }
           set { _check_out_operator = value; }
       }
    }
}

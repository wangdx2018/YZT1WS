using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace AFC.WS.Model.DB
{
    /// <summary>
    /// 客流监视
    /// </summary>
    public class PassengerMonitor
    {
        /// <summary>
        /// 交易日期
        /// </summary>
        private string _tran_date;

        /// <summary>
        /// 发行商id
        /// </summary>
        private decimal _issuer_id;
        /// <summary>
        /// 交易类型代码
        /// </summary>
        private string _afc_type;

        /// 设备类型
        /// </summary>
        string _device_type;
        /// <summary>
        /// 交易时间
        /// </summary>
        string _tran_time_min;
        /// <summary>
        /// 计数、统计。
        /// </summary>
        decimal _total;
        /// <summary>
        /// 交易类型名称。
        /// </summary>
        string _afc_type_name;

        /// <summary>
        /// 交易票价
        /// </summary>
        decimal _tran_value;

        /// <summary>
        /// 交易类型名称。
        /// </summary>
        public string Afc_type_name
        {
            get { return _afc_type_name; }
            set { _afc_type_name = value; }
        }
        /// <summary>
        /// 计数、统计。
        /// </summary>
        public decimal Total
        {
            get { return _total; }
            set { _total = value; }
        }

        /// <summary>
        /// 交易日期
        /// </summary>
        public string Tran_date
        {
            get { return _tran_date; }
            set { _tran_date = value; }
        }
        /// <summary>
        /// 交易时间
        /// </summary>
        public string Tran_time_min
        {
            get { return _tran_time_min; }
            set { _tran_time_min = value; }
        }
        /// <summary>
        /// 设备类型
        /// </summary>
        public string Device_type
        {
            get { return _device_type; }
            set { _device_type = value; }
        }

        /// <summary>
        /// 交易类型代码
        /// </summary>
        public string Afc_type
        {
            get
            {
                return this._afc_type;
            }
            set
            {
                this._afc_type = value;
            }
        }
        /// <summary>
        /// 发行商id
        /// </summary>
        public decimal Issuer_id
        {
            get
            {
                return this._issuer_id;
            }
            set
            {
                this._issuer_id = value;
            }
        }

        /// <summary>
        /// 交易票价
        /// </summary>
        public decimal Tran_value
        {
            get { return _tran_value; }
            set { _tran_value = value; }
        }

    }
}
